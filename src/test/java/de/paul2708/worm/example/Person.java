package de.paul2708.worm.example;

import de.paul2708.worm.columns.*;
import de.paul2708.worm.columns.generator.IntegerGenerator;

@Table("persons")
public class Person {

    @Column("id")
    @PrimaryKey
    @AutoGenerated(IntegerGenerator.class)
    private int id;

    @Column("name")
    @MaxLength(255)
    private String name;

    @Column("age")
    private int age;

    public Person() {

    }

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public int getId() {
        return id;
    }

	public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    @Override
    public String toString() {
        return "Person{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
